namespace LearnProgramming.Variable;

public class Variable
{
    //Değişken Adlamlandırması
    //Bir scope içerisinde aynı isimde değişken birden fazla tanımlanamaz.
    
    //Pascal Case
    int MyNumber;
    string IO;
    
    //Camel Case
    int myNumber;
    string io;
    
    //Snake Case
    
    //Küçük harfle başlar ve her kelime için başında büyük harf kullanılır.
    int my_number;
    string i_o;
    
    //Değişkenleri @ ile tanımlama
    //Değişken isimlerinde programatik keyword kullanılamaz!
    //Fakat programatik keyword kullanmak istiyorsak @ işareti ile tanımlayabiliriz.
    string @static;

    /*
     * Global Variable
     * Bir değişken class scope içerisinde tanımlanmış ise bu değişken global değişkendir.
     */
    /*
     * Global vs Local Variable
     * https://medium.com/@icodewithben/global-vs-local-variables-bb38790523de
     */
}